# Variable

my_string_variable = "My string variable"
print(my_string_variable)

my_int_variable = 5
print(my_int_variable)

my_bool_variable = True
print(my_bool_variable)

my_float_variable = 5.5
print(my_float_variable)

my_list_variable = [1, 2, 3, 4, 5]
print(my_list_variable)

my_dict_variable = {"key1": "value1", "key2": "value2"}
print(my_dict_variable)

my_tuple_variable = (1, 2, 3)
print(my_tuple_variable)

my_set_variable = {1, 2, 3}
print(my_set_variable)

my_none_variable = None
print(my_none_variable)

print(my_int_variable, my_float_variable, my_string_variable, my_bool_variable, my_list_variable, my_dict_variable, my_tuple_variable, my_set_variable, my_none_variable)

print(len(my_dict_variable))


### Operadores ####

# Operadores Aritméticos

print(2 + 3) # Suma
print(2 - 3) # Resta
print(2 * 3) # Multiplicación
print(2 / 3) # División
print(2 // 3) # División entera
print(2 % 3) # Módulo
print(2 ** 3) # Potencia


# Operadores de Comparación

print(2 == 3) # Igual
print(2 != 3) # Diferente
print(2 > 3) # Mayor
print(2 < 3) # Menor
print(2 >= 3) # Mayor o igual
print(2 <= 3) # Menor o igual



# Operadores Lógicos

print(True and False) # Y
print(True or False) # O
print(not True) # No


# Operadores de Asignación

x = 5 # Asignación
x += 5 # Asignación con suma
x -= 5 # Asignación con resta
x *= 5 # Asignación con multiplicación
x /= 5 # Asignación con división
x //= 5 # Asignación con división entera
x %= 5 # Asignación con módulo
x **= 5 # Asignación con potencia





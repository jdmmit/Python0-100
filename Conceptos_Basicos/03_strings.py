### Strings ###

my_string = "Mi string"
mu_other_string = "Mi otro string"

print(len(my_string))  # Length of the string
print(my_string + " " + mu_other_string)  # Concatenation of strings

my_new_string = "este es un string \n con salto de linea"
print(my_new_string)  # Print the string with a newline character

my_tab_string = "este es un string \t con tabulacion"
print(my_tab_string)  # Print the string with a tab character

my_scape_string = "\\ este es un string \\ con barra invertida"
print(my_scape_string)  # Print the string with a backslash character